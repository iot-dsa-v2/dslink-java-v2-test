package org.iot.dsa.dslink.test;

import com.acuity.iot.dsa.dslink.test.V1TestLink;
import org.iot.dsa.dslink.DSLink;
import org.iot.dsa.dslink.DSLinkConnection;
import org.iot.dsa.dslink.test.subscribe.Qos1Test;
import org.iot.dsa.dslink.test.subscribe.Subscriptions;
import org.testng.Assert;
import org.testng.annotations.Test;

public class SubscriptionTest implements DSLinkConnection.Listener {

    ///////////////////////////////////////////////////////////////////////////
    // Class Fields
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Instance Fields
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Constructors
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Public Methods
    ///////////////////////////////////////////////////////////////////////////

    @Override
    public void onConnect(DSLinkConnection connection) {
        synchronized (this) {
            notify();
        }
    }

    @Override
    public void onDisconnect(DSLinkConnection connection) {
    }

    @Test
    public void test() {
        MainNode root = new MainNode();
        DSLink link = new V1TestLink(root);
        link.getConnection().addListener(this);
        Thread t = new Thread(link, "DSLink Runner");
        t.start();
        waitForConnection();
        Assert.assertTrue(link.getConnection().isConnected());
        Qos1Test qos1 = (Qos1Test) root.getNode(MainNode.SUBSCRIPTIONS).getNode(Subscriptions.QOS1);
        Assert.assertTrue(qos1.test(10, 1000000, 0));
    }

    ///////////////////////////////////////////////////////////////////////////
    // Protected Methods
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Package / Private Methods
    ///////////////////////////////////////////////////////////////////////////

    private synchronized void waitForConnection() {
        try {
            wait(10000);
        } catch (InterruptedException x) {
            throw new RuntimeException(x);
        }
    }

    ///////////////////////////////////////////////////////////////////////////
    // Inner Classes
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Initialization
    ///////////////////////////////////////////////////////////////////////////

}
